$scopeAfterCompanyId = @'
  `scope_type` enum('company','group') NOT NULL DEFAULT 'company' COMMENT 'Tenant scope: company or group ledger',
  `scope_id` bigint UNSIGNED DEFAULT NULL COMMENT 'Numeric scope: company.id or groups.id',
'@

function Inject-ScopeAfterCompanyId {
    param([string]$Block, [string]$CompanyIdLinePattern)
    if ($Block -notmatch [regex]::Escape($CompanyIdLinePattern)) { return $Block }
    return $Block.Replace($CompanyIdLinePattern, ($CompanyIdLinePattern + "`r`n" + $scopeAfterCompanyId.TrimEnd()))
}

$scopeInjectPattern = '(?ms)(CREATE TABLE `(?:account_company|currency|data_captures|data_capture_details|data_capture_templates|description|transactions|transactions_deleted|transaction_entry|user_company_map)` \((?:(?!`scope_type`).)+?)(  `company_id`[^\n]+)(\n)'
$scopeTail = ($scopeAfterCompanyId.TrimEnd() -replace ',\s*$', '')
$sql = [regex]::Replace($sql, $scopeInjectPattern, {
    param($m)
    $companyLine = $m.Groups[3].Value.TrimEnd()
    if ($companyLine -notmatch ',$') { $companyLine += ',' }
    $m.Groups[1].Value + $m.Groups[2].Value + $companyLine + "`n" + $scopeTail + "`n"
})
# Fix stray comma-only lines from scope injection
$sql = $sql -replace ",\r?\n,\r?\n", ",`n"
$sql = $sql -replace "(`scope_id`[^\r\n]+),\r?\n(\) ENGINE)", "`$1`n`$2"

# company: auto-renew + note on group_id (regex â€” line endings normalized to LF)
$sql = [regex]::Replace($sql, '(?ms)(CREATE TABLE `company` \(.+?`group_id` varchar\(50\) DEFAULT NULL)\s*(\) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;)', {
    param($m)
    $m.Groups[1].Value + " COMMENT 'Parent group code for subsidiary companies only (not group definition)',`n" +
    "  ``auto_renew_enabled`` tinyint(1) NOT NULL DEFAULT 0 COMMENT 'Subscription auto-renew enabled',`n" +
    "  ``auto_renew_period`` varchar(20) DEFAULT NULL COMMENT '7days|1month|3months|6months|1year',`n" +
    "  ``payment_customer_id`` varchar(255) DEFAULT NULL,`n" +
    "  ``payment_subscription_id`` varchar(255) DEFAULT NULL,`n" +
    "  ``auto_renew_updated_at`` datetime DEFAULT NULL,`n" +
    "  ``auto_renew_updated_by`` varchar(50) DEFAULT NULL`n" +
    $m.Groups[2].Value
})

# domain_list_fee_settings
$sql = [regex]::Replace($sql, '(?ms)CREATE TABLE `domain_list_fee_settings` \([\s\S]*?\) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;', @'
